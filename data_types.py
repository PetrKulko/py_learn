# Data types

# Integer (int)
int_items = 3, 12, 24

# Floating points (float)
float_items = 4.02, -0.23, 1.2

# String (str)
str_items = "Hello", "How are you?"

# List (list)
list_items = ["Hello", 12, 98.02]

# Dictionary (dict) - "key-values"
dict_items = {"luckyNumber": "444", 'name': 'Peter'}

# Tuple (tup) immutable order objects
tuple_items = ("Hello", 12, 54.2, 12)

# Set unordered, unique objects
set_items = ("Hello", 12, 0.2)

# Boolean (bool)
bool_item = 'false'
